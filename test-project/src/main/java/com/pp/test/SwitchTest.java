public class SwitchTest {

    public static void main(String[] args) {


        String param = null;

        switch (param) {
            case "param":
                System.out.println("print param");
                break;
            case "String":
                System.out.println("print String");
                break;
            case "null":
                System.out.println("print null");
                break;
            default:
                System.out.println("default");
        }

    }

}
